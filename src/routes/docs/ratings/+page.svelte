<h2>Ratings</h2>
<br/><br/>
<h3>RXE</h3>
<p>
    RXE ratings range from 0-20,000 and are based on your chance to beat the median player.<br/>
    This means they may change over time as many players increase or decrease in rating.<br/>
    You will not get an RXE rating when the system is unsure of your skill.
</p>
<br/><br/>
<h3>GLK</h3>
<p>
GLK (glicko) is a rating system used on chess.com, similar to ELO.<br/>
After your rating you will see some symbols (??, ?, !, !!).<br/>
These indicate how sure the system is of your rating
</p>