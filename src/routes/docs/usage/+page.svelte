<h2>Basic usage</h2>

<p>
    To play chess in a channel, first the bot needs to be enabled in the channel.<br/>This is to prevent spam in non-bot channels.<br/>
    To enable the bot, run /enable in a channel. This requires the Manage Channels permission.<br/>
    To disable it, run /disable.
</p>
<br/><br/>
<p>
    To create a game, run /newgame.<br/>
    To make a move, run /move and write the move in <a href="https://en.wikipedia.org/wiki/Algebraic_notation_(chess)">SAN</a><br/>
    If a game is already running in the channel you want to play in, and it has been inactive for 5 minutes, you can cancel it with /cancelGame.<br/>
    This will allow others to make a new game to play.<br/>
    To resign a game, run /resign
</p>
<br/><br/>
<p>
    After a game, your ratings will update.<br/>
    For more information on ratings, see the ratings page.
</p>